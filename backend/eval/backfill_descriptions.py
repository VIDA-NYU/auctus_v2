"""Backfill AutoDDG description arms for already-ingested datasets.

Re-ingesting a dataset just to refresh its generated descriptions repeats the
expensive parts of the pipeline (download, profiling) for nothing: the full
profile — including the CSV sample the descriptions are generated from — is
already stored in MinIO. This script instead:

  1. downloads the full profile JSON from MinIO,
  2. regenerates the AutoDDG descriptions (UFD / SFD / LLM-direct + topic and
     semantic profile) with the exact same code path ingestion uses
     (``storage.arq_worker.attach_autoddg_description``),
  3. uploads the refreshed profile back to MinIO, and
  4. updates ONLY the description fields on the OpenSearch document in place.

That makes prompt / arm iteration (the plan-2 improvements) cost one LLM pass
instead of a full re-ingest, and never touches the original portal description
or the rest of the search document.

Run inside the worker environment (it needs autoddg, portkey, minio and the
OpenSearch client), e.g.:

    docker compose exec arq-worker python -m eval.backfill_descriptions --all
    docker compose exec arq-worker python -m eval.backfill_descriptions --ids abcd-1234 wxyz-5678
    python -m eval.backfill_descriptions --all --dry-run   # report only, no LLM calls

Previously generated AutoDDG fields are cleared before regeneration so a
partially failing run cannot leave a document mixing fields from two different
generations (the OpenSearch update writes ``null`` for arms that failed).
"""

from __future__ import annotations

import argparse
import json
import logging
import sys

from storage.arq_worker import AUTODDG_EVAL_ARMS, attach_autoddg_description
from storage.minio_client import (
    get_heavy_profile_object,
    get_storage_client,
    upload_heavy_profile,
)
from storage.opensearch_client import AUCTUS_INDEX_NAME, get_client

LOGGER = logging.getLogger("backfill_descriptions")

# Fields regenerated by attach_autoddg_description. The searchable evaluation
# arms are mirrored into OpenSearch; the intermediates are kept only in the
# MinIO full profile. Only fields this run will actually (re)generate may be
# cleared or written: with AUTODDG_EVAL_ARMS=0 the worker SKIPS the LLM-direct
# arm, and clearing it here would silently destroy previously generated values.
SEARCH_FIELDS = (
    "autoddg_description",
    "autoddg_search_description",
)
if AUTODDG_EVAL_ARMS:
    SEARCH_FIELDS += ("llm_direct_description",)
INTERMEDIATE_FIELDS = ("autoddg_topic", "autoddg_semantic_profile")
GENERATED_FIELDS = SEARCH_FIELDS + INTERMEDIATE_FIELDS


def list_all_ids(os_client, index: str) -> list[str]:
    """All document ids in the index (scroll, so >10k works)."""
    ids: list[str] = []
    resp = os_client.search(
        index=index, body={"query": {"match_all": {}}, "_source": False},
        size=1000, scroll="2m",
    )
    scroll_id = resp.get("_scroll_id")
    while True:
        hits = resp.get("hits", {}).get("hits", [])
        if not hits:
            break
        ids.extend(h["_id"] for h in hits)
        resp = os_client.scroll(scroll_id=scroll_id, scroll="2m")
        scroll_id = resp.get("_scroll_id")
    if scroll_id:
        try:
            os_client.clear_scroll(scroll_id=scroll_id)
        except Exception:
            pass
    return ids


def load_full_profile(storage_client, dataset_id: str) -> dict | None:
    try:
        obj, _name = get_heavy_profile_object(storage_client, dataset_id)
    except Exception as exc:
        LOGGER.warning("No MinIO profile for %s: %s", dataset_id, exc)
        return None
    try:
        return json.loads(obj.read().decode("utf-8"))
    finally:
        obj.close()
        obj.release_conn()


def backfill_one(os_client, storage_client, dataset_id: str, dry_run: bool) -> str:
    """Regenerate descriptions for one dataset. Returns a status string."""
    record = load_full_profile(storage_client, dataset_id)
    if record is None:
        return "no-profile"
    if not record.get("sample"):
        return "no-sample"

    if dry_run:
        present = [f for f in SEARCH_FIELDS if record.get(f)]
        return f"would regenerate (currently populated: {present or 'none'})"

    # Clear previous generations so the result reflects this run only.
    for field in GENERATED_FIELDS:
        record.pop(field, None)

    record = attach_autoddg_description(record)
    if not any(record.get(f) for f in SEARCH_FIELDS):
        # Nothing was generated (no client / all calls failed): keep the old
        # profile and search doc rather than blanking them.
        return "generation-failed"

    upload_heavy_profile(storage_client, dataset_id, record)
    os_client.update(
        index=AUCTUS_INDEX_NAME,
        id=dataset_id,
        body={"doc": {f: record.get(f) for f in SEARCH_FIELDS}},
        refresh=True,
    )
    populated = [f for f in SEARCH_FIELDS if record.get(f)]
    return f"updated ({', '.join(populated)})"


def main(argv: list[str] | None = None) -> int:
    logging.basicConfig(level=logging.INFO, format="%(levelname)s %(name)s: %(message)s")
    parser = argparse.ArgumentParser(description=__doc__)
    group = parser.add_mutually_exclusive_group(required=True)
    group.add_argument("--ids", nargs="+", help="Dataset ids to backfill")
    group.add_argument(
        "--all", action="store_true", help="Backfill every document in the index"
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Report what would be regenerated; no LLM calls, no writes",
    )
    args = parser.parse_args(argv)

    os_client = get_client()
    storage_client = get_storage_client()

    ids = args.ids or list_all_ids(os_client, AUCTUS_INDEX_NAME)
    print(f"Backfilling {len(ids)} dataset(s){' [dry-run]' if args.dry_run else ''}")
    if not AUTODDG_EVAL_ARMS:
        print(
            "AUTODDG_EVAL_ARMS is off: llm_direct_description is left untouched "
            "(not regenerated, not cleared)."
        )

    counts: dict[str, int] = {}
    failures = 0
    for i, dataset_id in enumerate(ids, 1):
        try:
            status = backfill_one(os_client, storage_client, dataset_id, args.dry_run)
        except Exception as exc:
            LOGGER.exception("Backfill failed for %s: %s", dataset_id, exc)
            status = "error"
        # no-profile / no-sample are expected for seeded or sample-less docs and
        # only reported; real generation problems drive the exit code.
        if status in ("generation-failed", "error"):
            failures += 1
        counts[status.split(" ")[0]] = counts.get(status.split(" ")[0], 0) + 1
        print(f"  [{i}/{len(ids)}] {dataset_id}: {status}")

    print(f"\nDone. Status counts: {counts}")
    return 1 if failures else 0


if __name__ == "__main__":
    raise SystemExit(main())
