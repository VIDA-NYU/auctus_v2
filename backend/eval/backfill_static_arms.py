"""Backfill the two deterministic (LLM-free) benchmark-EDA description arms.

The AutoDDG arms (UFD / SFD / LLM-direct) are generated by an LLM; these two are
not, so they need no Portkey and can be filled offline:

  * ``profile_only_description`` — the trimmed content profile only, rendered by
    the SAME ``build_profile_text`` ingestion feeds to AutoDDG. Built from the
    OpenSearch document alone (``profiler_metadata`` + ``spatial_coverage``), so
    it needs no MinIO.
  * ``tods_description`` — the raw Title + Original-Description + data-Sample
    bundle, i.e. AutoDDG's inputs WITHOUT the LLM synthesis. The sample lives
    only in the MinIO full profile, so this arm needs MinIO; a dataset with no
    stored sample keeps whatever ``tods_description`` it already had.

Both arms complete the 6-column matrix so the AutoDDG arms can be compared
against un-synthesised baselines. This script only touches these two fields; it
never regenerates or clears the LLM arms or the original portal description.

Run inside the worker environment (needs opensearch; MinIO only for the T+OD+S
arm):

    docker compose exec arq-worker python -m eval.backfill_static_arms --all
    docker compose exec arq-worker python -m eval.backfill_static_arms --ids abcd-1234
    python -m eval.backfill_static_arms --all --dry-run   # report only, no writes
"""

from __future__ import annotations

import argparse
import logging

from storage.arq_worker import build_profile_text
from storage.minio_client import get_storage_client
from storage.opensearch_client import (
    AUCTUS_INDEX_NAME,
    GENERATED_DESCRIPTION_FIELD_MAPPINGS,
    get_client,
)

# load_full_profile / list_all_ids already do exactly what we need; reuse them
# instead of re-implementing the MinIO read and the scroll.
from eval.backfill_descriptions import list_all_ids, load_full_profile

LOGGER = logging.getLogger("backfill_static_arms")

PROFILE_ONLY_FIELD = "profile_only_description"
TODS_FIELD = "tods_description"
STATIC_ARM_FIELDS = (PROFILE_ONLY_FIELD, TODS_FIELD)

# Fields we need off the OpenSearch document to build the arms.
_OS_SOURCE_FIELDS = ["title", "description", "profiler_metadata", "spatial_coverage"]


def ensure_arm_field_mappings(os_client, index: str = AUCTUS_INDEX_NAME) -> None:
    """Explicitly map the two arm fields before any document carries them.

    If they were left to dynamic mapping they'd get the standard analyzer
    (no English stopwords) while ``description`` uses ``text_analyzer`` (with
    stopwords), skewing cross-arm BM25 — the exact bug the mapping single-source
    guards against. put_mapping is idempotent and safe on an existing index as
    long as no document has already dynamic-mapped the field (none has yet).
    """
    props = {
        f: GENERATED_DESCRIPTION_FIELD_MAPPINGS[f]
        for f in STATIC_ARM_FIELDS
        if f in GENERATED_DESCRIPTION_FIELD_MAPPINGS
    }
    os_client.indices.put_mapping(index=index, body={"properties": props})
    LOGGER.info("Ensured mappings for %s on %s", list(props), index)


def build_static_arms(os_source: dict, sample: str | None) -> dict[str, str]:
    """Return the deterministic arm fields buildable from the given inputs.

    ``profile_only`` is always attempted from the OS document; ``t_od_s`` is
    only produced when a data sample is available (otherwise it is omitted so the
    existing value is left untouched rather than blanked).
    """
    arms: dict[str, str] = {}

    profile_text = build_profile_text(os_source)
    if profile_text:
        arms[PROFILE_ONLY_FIELD] = profile_text

    if sample:
        parts = [
            os_source.get("title") or "",
            os_source.get("description") or "",
            str(sample),
        ]
        tods = "\n\n".join(p for p in parts if p.strip())
        if tods.strip():
            arms[TODS_FIELD] = tods

    return arms


def backfill_one(os_client, storage_client, dataset_id: str, dry_run: bool) -> str:
    """Build and write the two static arms for one dataset. Returns a status."""
    try:
        doc = os_client.get(
            index=AUCTUS_INDEX_NAME, id=dataset_id, _source=_OS_SOURCE_FIELDS
        )
    except Exception as exc:  # missing doc / transport error
        LOGGER.warning("No OpenSearch doc for %s: %s", dataset_id, exc)
        return "no-doc"
    os_source = doc.get("_source") or {}

    # The sample lives only in the MinIO full profile (never in the OS doc).
    record = load_full_profile(storage_client, dataset_id) if storage_client else None
    sample = record.get("sample") if isinstance(record, dict) else None

    arms = build_static_arms(os_source, sample)
    if not arms:
        return "nothing-to-build"

    if dry_run:
        return f"would set {', '.join(sorted(arms))}"

    os_client.update(
        index=AUCTUS_INDEX_NAME,
        id=dataset_id,
        body={"doc": arms},
        refresh=True,
    )
    missing = [f for f in STATIC_ARM_FIELDS if f not in arms]
    note = f" (no sample -> {TODS_FIELD} skipped)" if TODS_FIELD in missing else ""
    return f"updated ({', '.join(sorted(arms))}){note}"


def main(argv: list[str] | None = None) -> int:
    logging.basicConfig(level=logging.INFO, format="%(levelname)s %(name)s: %(message)s")
    parser = argparse.ArgumentParser(description=__doc__)
    group = parser.add_mutually_exclusive_group(required=True)
    group.add_argument("--ids", nargs="+", help="Dataset ids to backfill")
    group.add_argument(
        "--all", action="store_true", help="Backfill every document in the index"
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Report what would be written; no mapping change, no writes",
    )
    args = parser.parse_args(argv)

    os_client = get_client()
    # MinIO is only needed for the T+OD+S sample; degrade gracefully without it
    # so profile_only can still be backfilled from OpenSearch alone.
    try:
        storage_client = get_storage_client()
    except Exception as exc:
        LOGGER.warning("No MinIO client (%s); T+OD+S arm will be skipped", exc)
        storage_client = None

    if not args.dry_run:
        ensure_arm_field_mappings(os_client)

    ids = args.ids or list_all_ids(os_client, AUCTUS_INDEX_NAME)
    print(f"Backfilling static arms for {len(ids)} dataset(s)"
          f"{' [dry-run]' if args.dry_run else ''}")

    counts: dict[str, int] = {}
    failures = 0
    for i, dataset_id in enumerate(ids, 1):
        try:
            status = backfill_one(os_client, storage_client, dataset_id, args.dry_run)
        except Exception as exc:
            LOGGER.exception("Backfill failed for %s: %s", dataset_id, exc)
            status = "error"
        if status in ("no-doc", "error"):
            failures += 1
        counts[status.split(" ")[0]] = counts.get(status.split(" ")[0], 0) + 1
        print(f"  [{i}/{len(ids)}] {dataset_id}: {status}")

    print(f"\nDone. Status counts: {counts}")
    return 1 if failures else 0


if __name__ == "__main__":
    raise SystemExit(main())
